public class DiaDeSemanaSwitch {
    public static void main(String[] args) {
        byte number = 5;
        switch (number) {
            case 1:
                case7:
                System.out.println("Fim de semana");
                break;
            case 2:
            case 3:
            case 4:
            case 5:
            case 6:
                System.out.println("Dia de semana");
                break;
            default:
                System.out.println("número inválido");
        }
    }
}
